    private void setData(ArrayList<FileData> data) {
        float barWidth = 9f;
        float spaceForBar = 10f;

        ArrayList<BarDataSet> sets = new ArrayList<BarDataSet>();
        ArrayList<ArrayList<BarEntry>> entries = new ArrayList<ArrayList<BarEntry>>();
        ArrayList<String> names = new ArrayList<String>();

        for (int i = 0; i < data.size(); i++) {
            ArrayList<BarEntry> e = new ArrayList<BarEntry>();
            e.add(0, new BarEntry(i * spaceForBar, data.get(i).getLength()));
            entries.add(i, e);
            names.add(data.get(i).getName());
        }

        for (int i = 0; i < data.size(); i++) {
            BarDataSet set = new BarDataSet(entries.get(i), names.toString());
            set.setDrawValues(true);
            set.setValueTextColor(Color.BLACK);
            set.setValueTextSize(12f);

            sets.add(i, set);
        }



        ArrayList<IBarDataSet> dataSets = new ArrayList<IBarDataSet>();
        for (int i = 0; i < data.size(); i++) {
            dataSets.add(sets.get(i));
        }

        //dataSets.add(set1);

        BarData bData = new BarData(dataSets);
        bData.setValueTextSize(10f);
        bData.setValueTypeface(mTfLight);
        bData.setBarWidth(barWidth);
        mChart.setData(bData);
        mChart.setDescription("");    // Hide the description
        mChart.getAxisLeft().setDrawLabels(false);
        mChart.getAxisRight().setDrawLabels(false);
        mChart.getXAxis().setDrawLabels(false);
        mChart.getLegend().setEnabled(false);   // Hide the legend
    }

    private void setData(int count, float range) {

        float barWidth = 9f;
        float spaceForBar = 10f;
        ArrayList<BarEntry> yVals1 = new ArrayList<BarEntry>();

        for (int i = 0; i < count; i++) {
            float val = (float) (Math.random() * range);
            yVals1.add(new BarEntry(i * spaceForBar, val));
        }

        BarDataSet set1;

        if (mChart.getData() != null &&
                mChart.getData().getDataSetCount() > 0) {
            set1 = (BarDataSet)mChart.getData().getDataSetByIndex(0);
            set1.setValues(yVals1);
            mChart.getData().notifyDataChanged();
            mChart.notifyDataSetChanged();
        } else {
            set1 = new BarDataSet(yVals1, "DataSet 1");

            ArrayList<IBarDataSet> dataSets = new ArrayList<IBarDataSet>();
            dataSets.add(set1);

            BarData data = new BarData(dataSets);
            data.setValueTextSize(10f);
            data.setValueTypeface(mTfLight);
            data.setBarWidth(barWidth);
            mChart.setData(data);
        }
    }

    protected RectF mOnValueSelectedRectF = new RectF();
    @SuppressLint("NewApi")
    @Override
    public void onValueSelected(Entry e, Highlight h) {

        if (e == null)
            return;

        RectF bounds = mOnValueSelectedRectF;
        mChart.getBarBounds((BarEntry) e, bounds);

        MPPointF position = mChart.getPosition(e, mChart.getData().getDataSetByIndex(h.getDataSetIndex())
                .getAxisDependency());

        Log.i("bounds", bounds.toString());
        Log.i("position", position.toString());

        MPPointF.recycleInstance(position);
    }

